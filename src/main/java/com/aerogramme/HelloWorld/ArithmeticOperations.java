package com.aerogramme.HelloWorld;

public class ArithmeticOperations {

    public static void main(String[] args) {

        int num1 = 100;
        int num2 = 20;

        // add
        System.out.println("Num1 + Num2 = " + (num1 + num2));
        // divide
        System.out.println("Num1 / Num2 = " + (num1/num2));
        // multiple
        System.out.println("Num1 * Num2 = " +  (num1 * num2));
        // subtract
        System.out.println("Num1 - Num2 = " + (num1 - num2));
        // modulus
        System.out.println("Num1 % Num2 = " + (num1 % num2));



    }
}
